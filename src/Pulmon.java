public class Pulmon {
    private int capacidadPulmonar;
    private String estadoSalud;
    private boolean esIzquierdo;

    public int getCapacidadPulmonar() {
        return capacidadPulmonar;
    }

    public void setCapacidadPulmonar(int capacidadPulmonar) {
        this.capacidadPulmonar = capacidadPulmonar;
    }

    public String getEstadoSalud() {
        return estadoSalud;
    }

    public void setEstadoSalud(String estadoSalud) {
        this.estadoSalud = estadoSalud;
    }

    public boolean isEsIzquierdo() {
        return esIzquierdo;
    }

    public void setEsIzquierdo(boolean esIzquierdo) {
        this.esIzquierdo = esIzquierdo;
    }
}

